package beans;

public class JogadoresBean {
	private String nomeJog, posiJog, timeAtu;
	private int golsJog, idadeJog, idJog, numJog;
	private double pesoJog, altJog;
	
	public int getNumJog() {
		return numJog;
	}
	public void setNumJog(int numJog) {
		this.numJog = numJog;
	}	
	public int getIdJog() {
		return idJog;
	}
	public void setIdJog(int idJog) {
		this.idJog = idJog;
	}
	public String getNomeJog() {
		return nomeJog;
	}
	public void setNomeJog(String nomeJog) {
		this.nomeJog = nomeJog;
	}
	public String getPosiJog() {
		return posiJog;
	}
	public void setPosiJog(String posiJog) {
		this.posiJog = posiJog;
	}
	public String getTimeAtu() {
		return timeAtu;
	}
	public void setTimeAtu(String timeAtu) {
		this.timeAtu = timeAtu;
	}
	public int getGolsJog() {
		return golsJog;
	}
	public void setGolsJog(int golsJog) {
		this.golsJog = golsJog;
	}
	public int getIdadeJog() {
		return idadeJog;
	}
	public void setIdadeJog(int idadeJog) {
		this.idadeJog = idadeJog;
	}
	public double getPesoJog() {
		return pesoJog;
	}
	public void setPesoJog(double pesoJog) {
		this.pesoJog = pesoJog;
	}
	public double getAltJog() {
		return altJog;
	}
	public void setAltJog(double altJog) {
		this.altJog = altJog;
	}
}
